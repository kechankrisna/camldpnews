class Place {
  final int id;
  final String name;
  final String shortDescription;
  final String longDescription;
  final int star;
  final double latitude;
  final double longitude;
  final String image;
  final bool isLocalImage;
  Place({
    this.id,
    this.name,
    this.shortDescription,
    this.longDescription,
    this.star,
    this.latitude,
    this.longitude,
    this.isLocalImage,
    this.image,
  });
}
